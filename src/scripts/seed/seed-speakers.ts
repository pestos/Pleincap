async function seedSpeakers() {
  console.log('ğŸ¯ Starting Speakers seed script...\n')

  const payload = await getPayload({ config })

  let created = 0
  let skipped = 0
  let errors = 0

  for (const speaker of speakersData) {
    try {
      console.log(`Processing: ${speaker.name}`)

      // Generate slug from name (will be auto-generated by hook, but check for duplicates)
      const slug = speaker.name
        .normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '')
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/^-+|-+$/g, '')

      // Check if speaker already exists
      const existing = await payload.find({
        collection: 'speakers',
        where: { slug: { equals: slug } },
        limit: 1,
      })

      if (existing.docs.length > 0) {
        console.log(`  âœ“ Already exists (skipped)\n`)
        skipped++
        continue
      }

      // Upload photo to Media collection
      const photoId = await uploadMediaFromUrl(payload, speaker.image, speaker.name)

      // Convert bio to richText format
      const bioRichText = {
        root: {
          type: 'root',
          children: [
            {
              type: 'paragraph',
              children: [
                {
                  type: 'text',
                  text: speaker.bio,
                },
              ],
            },
          ],
          direction: 'ltr',
          format: '',
          indent: 0,
          version: 1,
        },
      }

      // Create speaker
      await payload.create({
        collection: 'speakers',
        data: {
          name: speaker.name,
          specialty: speaker.specialty,
          bio: bioRichText,
          photo: photoId,
        },
      })

      console.log(`  âœ“ Created successfully\n`)
      created++
    } catch (error) {
      console.error(`  âœ— Error: ${error instanceof Error ? error.message : String(error)}\n`)
      errors++
    }
  }

  console.log('â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”')
  console.log('ğŸ“Š Speakers Migration Summary')
  console.log('â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”')
  console.log(`Total speakers: ${speakersData.length}`)
  console.log(`âœ“ Created: ${created}`)
  console.log(`â†· Skipped: ${skipped}`)
  console.log(`âœ— Errors: ${errors}`)
  console.log('â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”\n')

  process.exit(errors > 0 ? 1 : 0)
}

